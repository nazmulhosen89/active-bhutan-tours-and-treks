Tourists can book a  package holiday to Bhutan through both International and Bhutanese local tour  operators. Information is available from travel agencies.<br /><br />
  The Royal Government of  Bhutan sets minimum selling prices for packages to Bhutan and this must be paid  in US dollars prior to arrival in Bhutan.<br /><br />

  <b style="color:#eb731a">Daily  Tariff</b><br /><br />

The minimum tariff for  tourist visiting in a group of 3 persons or more are as follows:<br />
  <strong>High Season Tariff</strong>&nbsp;– US$ 200 per  person per night halt ( February, March, April, May, August, September,  October, November and December).<br /><br />
  <strong>Low Season Tariff</strong>&nbsp;– US$165 per  person per night halt (January, June, July).<br />
  The minimum price  includes:<br />
<ul>
  <li>All internal taxes and charges (including the royalty)</li>
  <li>Accommodation</li>
  <li>All Meals</li>
  <li>All travel with a licensed Bhutanese Tour Guide</li>
  <li>All Internal Transport</li>
  <li>Camping Equipment and Haulage for Trekking Tours</li>
</ul>
The rates given above  are applicable per tourist per night halt in Bhutan. On the day of departure,  the &lsquo;local agents&rsquo; host obligation shall be limited to breakfast only and any  extra requirements shall be payable on actual basis.<br />
  The rates shall apply  uniformly irrespective of locations and the type of accommodation  provided/asked for. List of hotels and lodges approved for international  tourist accommodation updated from time to time shall be issued by TCB.<br /><br />
<strong>Note:&nbsp;</strong>Minimum package cost is  revised starting 1st&nbsp;January 2012. USD$ 250 per person per  night for the months of March, April, May, September, October, and November.  And USD$200 per person per night for the months of January, February, June,  July, August, and December. For more information please check with TCB.<br /><br />

