  <b style="color:#eb731a">Visa  Formalities</b><br /><br />

Other than Indian,  Bangladeshis and<em>&nbsp;</em>Maldivian nationals, all visitors to Bhutan require  a visa; all visas are issued from Thimphu; visas are only issued to tourists  booked with a local licensed tour operator, directly or through a foreign  travel agent. Applications for tourist visas are submitted by the tour  operator. Visa clearance from Thimphu must be obtained before coming to Bhutan.  Visa clearance takes at least 10 days to process. Air tickets to Bhutan cannot  be purchased without visa clearance. At your point of entry the visa will be  stamped in your passport on payment of US$20, two passport photos will also be  required. Visas are issued for a 15day period; extensions can be obtained in  Thimphu at a cost of Nu.510.<br /><br />

  <b style="color:#eb731a">Cancellations</b><br /><br />

Tour Programmes booked  and subsequently cancelled shall be subject to cancellation charges as follows:<br /><br />
<ul>
  <li>within 30 days of start of programme ~ no charges</li>
  <li>within 21 days ~ 10% of rate</li>
  <li>within 14 days ~ 15% of rate</li>
  <li>within 7 days ~ 30% of rate</li>
  <li>less than 7 days or without notice ~ 50% of rate</li>
  <li>after arrival in Bhutan ~ 100%</li>
</ul>

  <b style="color:#eb731a">Delayed  Arrivals</b><br /><br />

There is no charge for delays  in arrival and departure because of weather conditions disrupting flights or  road blocks. The tourist must however bear the cost of food, accommodation,  transportation, and other services required.<br /><br />
<strong>Note: INR (Indian Rupees) denominations of 500 and 1000 are not  accepted in Bhutan.</strong>




