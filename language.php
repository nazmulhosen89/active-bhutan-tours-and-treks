Bhutan is linguistically  rich with over eighteen dialects being spoken in the country. The richness of  the linguistic diversity can be attributed to the geographical disposition of  the country with its high mountain passes and deep valleys that contributed to  their survival.<br /><br />
The national language is  Dzongkha, which is the native language of the Ngalops of western Bhutan.  Dzogkha literally means the language spoken in the Dzongs and administrative  centers of Bhutan.<br />
  The other major  languages are the Tshanglakha and the Lhotshamkha. Tshanglakha is the native  language of the Tshanglas of eastern Bhutan while Lhotshamkha is spoken by the  southern Bhutanese of Nepali origin.<br /><br />
Other dialects spoken  are the Khengkha spoken by the Khengpas of Central Bhutan, the Bumthapkha  spoken by the Bumthaps, the Mangdepkah spoken by the inhabitants of Trongs and  the Cho Cha Nga Chang kha spoken by the Kurtoeps. The Sherpas, Lepchas and the  Tamangs in southern Bhutan also have their own dialect. Dialects that is on the  verge of becoming extinct is the Monkha and the Gongduepkha.<br /><br />

