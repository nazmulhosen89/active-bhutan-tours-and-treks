<b style="color:#eb731a">The way to Bhutan</b><br /><br />
Till Bhutan embarked on a planned economic development in the early 1960's, Bhutan by and large remained cut off from the rest of the world. The country was accessible only by foot. The two main entry points to the country was from the North and from the South. From the north, the entry was from Tibet and was possible crossing the high passes while from the South it was through the plains of Assam and West Bengal. However, with the planned economic development, accessibility became easy with motorable roads connecting Bhutan with the other parts of the country.<br /><br />

Today the main points of entry are through Phuentsholing in the south that links Bhutan with the Indian plains of West Bengal, through Gelephu and Samdrup Jongkhar that links with the Indian state of Assam and through Paro, where the entry is through Druk Air, the National airline of Bhutan.<br /><br />

<b style="color:#eb731a">Travel by Air</b><br /><br />
The country has so far only one airport that caters to the needs of visitors coming in through flight. However, an international airport is under construction in Gelephu that will eventually provide passengers with a choice of their entry to Bhutan.  The Bhutan's only international airport at Paro, is located at a height of 7300 ft above sea level and surrounded by mountains and hills as high as 16,000 feet. Druk Air is the national air operating with two A319.  It flies to destinations that include Bangkok, Delhi, Kolkata, Bodh Gaya, Dacca, Kathmandu and Guwahati in Assam and Plans are also underway to invite other private airliners.
The flight between Paro and Kathmandu is one of the most exciting ones as the aircraft passes over 4 of the 5 highest mountains in the world. In fine weather, as you soar higher up, you can enjoy the spectacular display of Mt. Everest, Lhotse, Makalu und Kangchenjunga at their best.<br /><br />

<b style="color:#eb731a">Travel by Land</b><br /><br />
Phuntsholing, Gelephu and Samdrup Jongkhar in eastern Bhutan are the only land border areas open for international tourists. The town of Phuntsholing is located approximately 170 km east of the Indian national airport Bagdogra. After crossing Phuntsholing, your journey then begins its mountainous climb through endless turns and hair-pin bends till you enter Thimphu, the capital city. The travel time for the 176 km stretch can be more than 6 hours.<br /><br />
Gelephu in South-Central Bhutan is another entry point to Bhutan. It is approximately 250 kms from Thimphu and the journey will take you through the sub-tropical areas of Bhutan before entering the alpine zone and then finally into Thimphu. One will have to traverse across three Districts and the travel time will be about ten hours.<br /><br />

Samdrup Jongkhar is the only entry point in eastern Bhutan. The town borders the Indian district of Darranga, Assam and is approximately 150 kms away from Guwahati, the capital city of Assam. The journey from Guwahati is about three hours. Tourists entering Bhutan through Samdrup Jongkhar will take you to Trashigang, the largest District in the country, and from there over the lateral route to Mongar, Bumthang, Trongsa, Wangde Phodrang and then finally into capital, Thimphu. The distance is about 700 kms and will take you a minimum of three days to reach Thimphu.<br /><br />

