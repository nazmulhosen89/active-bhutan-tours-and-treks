<b style="color:#eb731a">National  Emblem</b><br />The National Emblem of  Bhutan is a circle that projects a double diamond thunderbolt placed above the  lotus. There is a jewel on all sides with two dragons on vertical sides. The  thunderbolts represent the harmony between secular and religious power while the  lotus symbolizes purity. The jewel signifies the sovereign power while the  dragons (male and female) stands for the name of the country Druk yul or the  Land of the Dragon.<br /><br /><br /><br />

<b style="color:#eb731a">National  Flag</b><br />

<img src="images/sym_2.png" style="float:right; margin-left:10px;"/>
The National flag is  rectangle in shape that is divided into two parts diagonally. The upper yellow  half signifies the secular power and authority of the king while the lower  saffron-orange symbolizes the practice of religion and power of Buddhism,  manifested in the tradition of Drukpa Kagyu. The dragon signifies the name and  the purity of the country while the jewels in its claws stand for the wealth  and perfection of the country.<br /><br /><br /><br /><br />

<img src="images/sym_3.png" style="float:left; margin-right:10px;"/><b style="color:#eb731a">National  Flower</b><br />The national flower is  Blue Poppy (Meconopsis Grandis). It is a delicate blue or purple tinged blossom  with a white filament. It grows to a height of 1 meter, on the rocky mountain  terrain found above the tree line of 3500-4500 meters. It was discovered in  1933 by a British Botanist, George Sherriff in a remote part of Sakteng in  eastern Bhutan.<br /><br /><br /><br /><br />

<img src="images/sym_4.png" style="float:right; margin-left:10px;"/>
<b style="color:#eb731a">National  Tree</b><br />The national tree is  cypress (Cupressus torolusa). Cypresses are found in abundance and one may  notice big cypresses near temples and monasteries. Cypress is found in the  temperate climate zone, between 1800 and 3500 meters. Its capacity to survive  on rugged harsh terrain is compared to bravery and simplicity.<br /><br /><br /><br /><br />

<img src="images/sym_5.png" style="float:left; margin-right:10px;"/>
<b style="color:#eb731a">National  Bird</b><br />The national bird is the  raven. It ornaments the royal crown. Raven represents the deity Gonpo  Jarodongchen (raven headed Mahakala), one of the chief guardian deities of  Bhutan.<br /><br /><br /><br /><br /><br /><br />

<img src="images/sym_6.png"  style="float:right; margin-left:10px;"/>
<b style="color:#eb731a">National  Animal</b><br />The national animal is  the Takin (burdorcas taxicolor) that is associated with religious history and  mythology. It is a very rare mammal with a thick neck and short muscular legs.  It lives in groups and is found in places above 4000 meters high on the  north-western and far north eastern parts of the country. They feed on bamboos.  The adult takin can weigh over 200 kgs.<br /><br /><br /><br />

  <b style="color:#eb731a">National  language</b><br />
Bhutan is a  multi-lingual society. Today, about 18 languages and dialects are spoken all  over the country. The state language is Dzongkha which in the olden times was  spoken by people who worked in the Dzongs that was the seat of temporal and  spiritual power. Later, Dzongkha was introduced as the national language of  Bhutan.<br /><br />

  <b style="color:#eb731a">National  Anthem</b><br />

The national anthem was  first composed in 1953 and became official in 1966. It is known as Druk  Tshenden Kepay Gyalkhab Na (In the land of the Dragon Kingdom, where cypress  grows).<br /><br />

  <b style="color:#eb731a">National  Day</b><br />

17th December is  celebrated as the National Day of the country that coincides with the crowning  ceremony of Gongsa Ugyen Wangchuck as the first hereditary king of Bhutan, in  Punakha Dzong on 17 December 1907. It is a national holiday and every Bhutanese  celebrates the day with pomp and festivity throughout the country.<br /><br />

