<b style="color:#eb731a">Traveling within Bhutan</b><br /><br />
Despite the major hurdle posed by the inaccessible terrain, the high mountain passes, all major towns and important places of visit are connected by a good road network. However, an important feature of the road system is the innumerable curves and bends that one will have to negotiate.  Besides the bends, another characteristic of the road network is the steep ascents and descents that slow down the car travels.   An average speed for road travel rarely exceeds 40 kilometers per hour, with tourist buses making even slower progress. One is however generously rewarded for the long and tiring car journey, by the spectacular views of the mountains, the lush green jungles, the ancient villages, the majestic temples and monasteries and the many road side restaurants and inns.<br /><br />

The roads are well sealed but the rides can still be bumpy as the lanes are single and narrow at most points. The advantage is that the Bhutanese are well accustomed to driving such lanes and know their land well and are careful drivers.<br /><br />

<b style="color:#eb731a">Tourist Buses:</b><br /><br />
Tourists can travel in Bhutan with medium-sized buses (20-22 seats), small buses (8-12 seats) or hired cars. Road widths do not permit larger buses.<br /><br />

<b style="color:#eb731a">Restricted area (Route) Permit</b><br /><br />
If you wish to travel to places other than Thimphu and Paro (permit free zone), you will have to obtain route permit from the immigration office in Thimphu (323127/F-321078.), for the tourists visiting Bhutan through our local Tour Operators the route permits are issued with the visa.<br /><br />

<b style="color:#eb731a">Special Permit</b><br /><br />
Although most of the religious places receive tourists, yet there are some restricted places. If you wish to visit those, you can do so, with a special permit. To acquire one, please contact Department of Culture (322694).<br /><br />

<b style="color:#eb731a">Bringing in your own vehicle</b><br /><br />
Indian visitors can bring in their own vehicles. For the entry of the foreign cars, the Road Safety and Transport Authority (RSTA): <a class="m" href="www.rsta.gov.bt">www.rsta.gov.bt</a> will have to endorse the documents. But we strongly recommend that you use our cars and the services of the local drivers as the narrow and winding roads in Bhutan may pose a challenge for you.<br />

For further information please visit www.mohca.gov.bt or email the Department of Immigration atbhutanimmi@druknet.bt




