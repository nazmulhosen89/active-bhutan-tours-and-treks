<div class="linkconttop">Samtengang Winter Trek </div>
<div style="float:left; padding:8px; height:auto; width:auto;">
This is another good trek for average walkers. It takes three days and give you a chance to visit the isolated valleys of Punakha and Wangdue, Altitude are low at 1500- 2000metres [5,000-6500feet] and it goes through serval villages,
This trek can be done in October, November, December, January, Feburary.
<br /><br />

  <b style="color:#eb731a">Day One</b>
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>

  
 
During this flight, one will experience breaktaking views of Mount Everest, Kanchan Junga and other Himalayan Peaks including sacred Chomolhari and Mt. Jichu Drake in Bhutan. On arrival at Paro International Airport you will be received by Dream Advanture Representative. Then drive to Hotel through Paro valley, an enchanting experience. Overnight at Hotel. 	 
 
<br><br>
  
  <b style="color:#eb731a">Day Two</b>
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>

  
Drive northwest up the Pa Chu to Drugyel Dzong, the ruined Fort, which once defended this valley from the Tibetan invasion. This fortress, now a burned shell, was once strategic in Bhutan's defense against the Tibetan invasion. Mount Chomolhari, the sacred summit, reaches skyward beyond the Dzong. This can be viewed on a clear day. On the way back you visit a typical Bhutanese farm house. After lunch walk up the hill just above Olathang Hotel to stretch your legs for the excursion to Taktsang view point. <br><br>
        
  <b style="color:#eb731a">Day Three</b>
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>

   
This morning follow the winding road beside the Pa Chu (Chu means river or water) down stream to its confluence with the Wangchu, then up valley to Thimphu, the capital. Afternoon lunch at the Hotel. After lunch visit the Memorial Chorten, here the faithful circumambulate the shrine dedicated to the Late King saying their prayer beads as they walk. Overnight at the Hotel	 
<br><br>
  
  <b style="color:#eb731a">Day Four</b>
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>

   
 
Drive to Khuruthang with pack lunch which will be provided at the Hotel. Stop at Dochula pass if the weather is clear to get a view of the snow capped Himalyayan mountains. Lunch at the camp and then drive to Punakha Dzong. Visit the Dzong if the Monks are in Thimphu. 	 
	 
<br><br>
  
  <b style="color:#eb731a">Day Five</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
Drive till Punakha Dzong. The trek begins with the gradual climb through Chir-pine forest. A stop to visit the village of Dompala can be organized depending on the group's choice. Distance 5/6 hours. Overnight in camp. <br><br>
  
  <b style="color:#eb731a">Day Six</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
The trail today takes you through Rhododendron and Oak forested areas. Enroute pass a famous Drukpa Kuenley Lhakhang. Overnight in camp. Distance - 14 Kms about 5/6 hours. <br><br>
  
   <b style="color:#eb731a">Day Seven</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
 
 
The trail now takes you through the village of Sha Wangdi district. Camp at Samtengang at the lake side. Distance 13 Kms about 5 hours. 					 
	 
<br><br>
  
   <b style="color:#eb731a">Day Eight</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
 
 
The trail now drops down till the motor road. Drive till Wangdiphodrang and lunch at the tourist lodge. Then continue to drive to Thimphu Overnight at the Hotel. 	 
	 
<br><br>
  
   <b style="color:#eb731a">Day Nine</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
 
 
A full day of sightseeing and shopping in Thimphu. Wander in the town, visit the Choki handicrafts & emporium. Visit the workshop where all the crafts are produced if you like. Afternoon visit the Tashichho-Dzong. Then drive outfor Motithang Hotel, take a walk up the hill behind the Hotel and visit the Takin penned up there. Overnight at the hotel. 	 
<br><br>
  
   <b style="color:#eb731a">Day Ten</b>
 <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
 
 
 
 
Drive to international Airport at Paro for onward flight. 					 
	 
