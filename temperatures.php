<strong>Rainfall</strong>
As a guide, rainfall in Thimphu and Paro is about 25mm a month in January to March, rising to 50 mm
in April and May, 75mm in June, and then a whopping 355mm in July, 300mm in August, 125mm in
September, back down to 75mm in October, 25 mm in November and 0 in December.
