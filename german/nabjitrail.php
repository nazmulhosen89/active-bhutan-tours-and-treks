<div class="linkconttop">Nabji Trail zwischen Trongsa und Zhemgang</div>
<div style="float:left; padding:8px; height:auto; width:auto;">

Eine 6-tägige Wanderung 6 tag Kultur(begrenzte Teilnehmerzahl/ 11 Personen pro Gruppe) von Dorf zu Dorf in Zentral-Bhutan zu den Minderheiten der Monpas, (People of the darkness) die als eine der Ersten das Land besiedelt haben. Der Trail führt zu ursprünglichen Dörfern, dschungelartigen Wäldern mit reichhaltiger Tier- u. Pflanzenwelt und vorbei an terrassenförmig angelegten Reisfeldern.<br /><br />

<b style="color:#eb731a">Tag 01 - Trekkingstart in Reotala</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Von Trongsa aus zweigt die Straße ab ins südlich gelegene Zhemgang. Nach ca. 3 stündiger Fahrt vorbei an einem wunderschönen Wasserfall und einer ehemaligen Winterresidenz des früheren Königs (Kuenga Rabten) gelangen wir nach Reotala, (1.060 m) wo die Wanderung beginnt. Nachdem wir den Fluß überquert haben, führt der Weg steil ansteigend hinauf zum ersten Dorf Nimshong, wo wir unser erstes Nachtlager aufschlagen. Das Dort hat ca. 465 Einwohner und eine kleine Schule.<br /><br />
Gehzeit ca. 3 - 4 Std. (6,5 km)
<br /><br />

<b style="color:#eb731a">Tag 02 - Weiter nach Nabji</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am frühen Morgen bringt uns der örtliche Führer des Dorfes zur kleinen Schule des Dorfes, wo wir den "Morgenappell" der Schüler beobachten können und etwas über das Schulwesen erfahren. Von dort aus geht es weiter durch einen üppigen Ur-Wald, in dem viele Vögel und Affen zu beobachten sind wie z.B. der Golden Langure und die riesigen Nashornvögel.<br /><br />
Nach einigen Stunden erreichen wir das kleine Dorf Nabji, das ca. 400 Einwohner zählt. Der Ort liegt inmitten von Reisterrassen, die sich weit den Hang hinauf ziehen. Der kleine Tempel wurde als ein Zeichen des Friedens zwischen Bumthang und den Königen von Assam erbaut. Im Ortsbezirk finden sich außerdem viele Versteinerungen, die ein großer Heiliger hier hinterlassen hat.<br /><br />
Gehzeit ca. 4 - 6 Std. (13km)

<br /><br />

<b style="color:#eb731a">Tag 03 - Ausflug nach Korphu</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Heute steht ein Tagesausflug ins nahegelegene Korphu auf dem Programm. Korphu liegt auf einem Bergkamm in 1.500 m Höhe. Es ist ein wunderschöner Weg hinauf zu dem kleinen Ort und von dort oben hat man einen herrlichen Blick über das bewaldete Tal mit dem Fluß, der die Landschaft durchzieht bis hin zu den Bergen im Westen, um die sich div. Geschichten ranken. Korphu hat auch einen kleinen Tempel, in dem Reliquien des großen buddhistischen Meisters Pema Lingpa aufbewahrt werden. Am Nachmittag Rückkehr nach Nabji.<br /><br />

<b style="color:#eb731a">Geh Tag 04 - Ins Land der Monpa</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am heiligen Baum von Nabji startet die heutige Etappe zum nächsten Dorf Kudra. Der Weg führt wieder ansteigend durch dicht bewaldete Gegend mit vielerlei Pflanzenarten und teilweise undurchdringlichem Dschungel. Der winzige Ort Kudra (1.636 m hoch) liegt inmitten eines Waldes und die dort lebenden Bewohner gehören der Minderheit der Monpas an. Die Monpas gelten als eine der ersten Bewohner Bhutans und waren ursprünglich Jäger und Sammler. Heute bewirtschaften sie Felder mit Mais und flechten vor allem Dinge aus Bambus für den täglichen Gebrauch. <br /><br />
Gehzeit ca. 6 Std. (14 km)<br /><br />
zeit ca. 4 - 5 Std. (12 km)
<br /><br />

<b style="color:#eb731a">Tag 05 - Kudra - Jangbi</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Vorbei an dem kleinen Ort Phumzur, wo wir einige der einfachen Hütten besuchen um mehr über das Leben der dort ansässigen Monpas zu erfahren, geht's weiter durch Pinienhaine und vorbei an Maisfeldern bis nach Jangbi (1.368 m), das auch eine kleine Schule hat. Von unserem Zeltplatz aus haben wir einen schönen Blick über das weite Tal, während wir unseren letzten Abend mit den dort lebenden Dorfbewohnern feiern.<br /><br />
Gehzeit ca. 6 Std. (14 km)
<br /><br />

<b style="color:#eb731a">Tag 06 - Die letzte Etappe</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der letzte Tag der Wanderung führt noch einmal bergauf und dann hinunter zu einer Brücke, die den Mangde Fluß überquert und weiter bis nach Tongtongfey, einem kleinen Ort an der Hauptstraße, wo das Fahrzeug auf uns wartet. Rückfahrt nach Trongsa.<br /><br />
Gehzeit ca. 3 - 4 Std. (8 km)
