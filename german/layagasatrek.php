<div class="linkconttop">Laya Gasa Trek [13 Nächte 14 Tage]</div>
<div style="float:left; padding:8px; height:auto; width:auto;">

Dieser Trek dauert ca. 2 Wochen und führt vom Paro Tal (2.200m) zum hochgelegenen Laya Gebiet auf 4.000m bis zum tief gelegenen Punakha Tal (1.300m). Er führt am Fuße des Jumolhari (7.314m) vorbei, sie begegnen vielen Yakherden mit ihren Hirten und durchqueren verschiedene Vegetationszonen bis zum Mount Massgang (7.200m). Dann erreichen Sie das Dorf Laya, wo die Frauen schwarze Yakhaarkleider und spitze Bambushütchen tragen. Dieser Trek ist landschaftlich und kulturell einzigartig und kann von September bis November gemacht werden.<br /><br />
