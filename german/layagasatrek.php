<b style="color:#eb731a">Tag 01 - Trekking nach Sharna</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Kurze Fahrt zur Ruine des Drukgyel-Dzong, wo die Trekkingtour beginnt. Das Gepäck wird wasserdicht verpackt und auf Pferde verladen. Leichte Wanderung im lieblichen Hochtal des Paro Chu (Chu=Fluss). Auf beiden Seiten des Flusses verläuft der Weg durch Terrassenfelder und Wiesen, vorbei an kleinen Ansiedlungen und einem Armeeposten der unser Permit checkt. Am frühen Nachmittag erreichen wir unser erstes Camp in Sharna. (2.870 m).<br /><br />
Gehzeit ca. 5 Std; Aufstieg 515 m, Abstieg 220 m.
<br /><br />

<b style="color:#eb731a">Tag 02 - Thangthangkha	</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Früh brechen wir zu unserer ersten längeren Wanderung auf. Durch einen Wald von Kiefern, Ahorn, Eichen und Rhododendren geht es flussaufwärts durch den Jigme Dorji National Park vorbei an Chorten und über kleine Brücken. Ca. 2 Std. sind es bis zum Steinhaus Shing Karapa (3.110 m) auf einer Lichtung. Bald danach erreichen wir die Abzweigung zum Tremo La, dem alten Handelsweg zwischen Tibet und Bhutan. Heute können wir den ersten Blick auf Jhomolhari (7314 m) werfen, die Herrin der Götterberge und heiligsten Berg des Landes. Die schneebedeckte Kuppel taucht plötzlich vor uns auf, wie aus dem Nichts. Unser Lager errichten wir in Thangthangkha auf 3.630 m.<br /><br />
Gehzeit ca. 7 Std. , Aufstieg 900 m, Abstieg 150 m.

<br /><br />

<b style="color:#eb731a">Tag 03 - Jhomolhari Basecamp</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Vorbei an den ersten Yakherden, die friedlich auf den Wiesen am Fluß grasen, Manimauern und vereinzelten kleinen Bauernhäusern geht es zum Fuße des Jhomolhari-Massivs, wo wir vor der Ruine eines verlassenen Forts das Lager aufschlagen. Yakzucht gehört zur Lebensgrundlage der hier ansässigen Bewohner. Aus Yakmilch wird außerdem Käse und Butter hergestellt. Steinerne Chorten umgeben das Lager und ein kleiner Bach durchquert den Lagerplatz in Jangothang auf 4.040 m.<br /><br />
Gehzeit ca. 5 – 6 Std; Aufstieg 550 m, Abstieg 100 m.
<br /><br />

<b style="color:#eb731a">Tag 04 - Am Fuße der Göttin Jhomo</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen können wir den Sonnenaufgang direkt von unserem Zelt aus genießen. Entspannungstag im Lager mit der Möglichkeit zu Ausflügen zu einem hochgelegenen See bzw. zum Aufstieg einer der vorgelagerten Gipfel mit Blick auf Jhomolhari (7314m) und Jichu Drake (6989m)<br /><br />

<b style="color:#eb731a"> Tag 05 - Trekking nach Lingshi</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir verlassen dieses Tal, überqueren den Paro Chhu und steigen auf zum ersten Pass NyileLa mit 4890 m wo uns Gebetsfahnen umflattern. Wir überschreiten die Baumgrenze und betreten ein in Ost-West-Richtung verlaufendes Gletschertal. Nachdem wir einige Zeit am Bergrücken entlang gewandert sind, können wir nach einem kurzen Anstieg einen ersten Blick auf Tserim Kang (6789m) und das vor uns liegende Lingshi werfen. Der Weg bergab führt durch einen Wald von Rhododendren vorbei an der Gletscherzunge des Tserim Kang zum Fuße des Lingshi Dzongs, wo wir zwischen weidenden Yakherden das Camp aufschlagen.<br /><br />
Gehzeit ca. 5 - 6 Std. Aufstieg 820 m, Abstieg 910 m.

<br /><br />

<b style="color:#eb731a">Tag 06 - Trekking nach Shashepasa</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Nach einem steilen Anstieg zum kleinen Ort Lingshi, von wo aus früher ein reger Handel mit Tibet betrieben wurde, geht's weiter hoch auf 4.140 m um das kleine Dorf Goyak zu erreichen, das spektakulär direkt unter einer steil aufragenden Felswand liegt.<br /><br />
Diese Gegend ist bekannt für seine Heilpflanzen u.a. Cordyseps, die hier auf den Hängen wachsen. Wir wandern vorbei am romantischen Tal von Chebisa, an dessen Ende ein großer Wasserfall liegt und überqueren noch den Gobul La (4.440 m. In den Felsformationen oberhalb des Aufstiegsweges leben Herden von Blauschafen und Greifvögel nisten dort oben. Vom Pass steigen wir in ein Seitental mit dichtem Rhododendronwald bis zu einem Gebirgsbach ab, dann geht es erneut aufwärts zu einem Kamm (.4.120 m). Über einen schwierigen, teils nassen Weg durch Hochwald steigen wir zu unserem Lagerplatz Shakshepasa auf 3.980 m ab.<br /><br />
Gehzeit ca. 8 Std. Aufstieg 710 m, Abstieg 600 m.
<br /><br />

<b style="color:#eb731a">Tag 07 - Trekking nach Robluthang</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Bergrücken entlang wandernd sehen wir dann in der Ferne bereits die schneebedeckten Gipfel des Gangchhenta (Great Tiger Mountain 6840 m) aufsteigen, aber vorher müssen wir noch den Jhari La (4747m) überqueren um ins liebliche Tal von Tsheri Jathang zu gelangen, wo Yaks weiden und im Sommer die scheuen Takins zu beobachten sind. Über Balken gelangen wir über den mehrarmigen Fluss. Ein steiler Hang trennt uns noch von einem idyllischen Plateau, auf dem sich unser einsamer Lagerpplatz Robluthang (4.170 m) befindet.<br /><br />
Gehzeit ca. 6 -7 Std. Aufstieg 1.010 m, Abstieg 840 m.
<br /><br />

<b style="color:#eb731a">Tag 08 - Zu Füßen des "Tiger-Mountain"</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Ein langer und anstrengender Tag liegt vor uns. Früh verlassen wir das Lager, steigen zunächst einen etwas sumpfigen, später in Serpentinen aufwärts führenden Pfad bis etwa 4.400 m auf. Hier gelangen wir in ein großes Gletschertal und sehen bereits den Sinche La voraus.
Wir überqueren den letzten und höchsten Pass, den Sinche La (5005 m) von wo aus wir einen fantastischen Blick auf die umliegende Bergwelt genießen können. Nördlich von uns erhebt sich das markante Massiv des Gangchhenta (6.840 m) auch "Tigermountain" genannt. Der Abstieg führt vorbei an Wasserfällen und den Ausläufern eines Gletschers hinunter zum Fluß, wo wir auf einer herrlichen Wiese zwischen Zedernwäldern, eingerahmt von hoch aufragenden Gipfeln unser Nachtlager in Limithang beziehen. (4.140 m)<br /><br />
Gehzeit ca. 7 -8 Std. Aufstieg 910 m, Abstieg 930 m.

<br /><br />

<b style="color:#eb731a">Tag 09 - Auf nach Laya</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Unser heutiges Etappenziel heißt Laya. Ein idyllischer Ort auf einem Hochplateau (3770 m) gelegen, wo die Frauen spezielle Trachten aus Yakwolle tragen und Hüte, die aus Bambus geflochten sind. Den Kango Chhu passieren wir kurz nach dem Lager, dann folgen einige kleine Seitenbäche in einem Zedernwald. Der Masang Gang (7165 m) taucht vor uns auf und begleitet uns auf einem eher gemächlichen Pfad durch Tannenwälder, der sich nach Laya hinauf windet. Wir nehmen den oberen Weg, um über den ganzen malerischen Ort und seine weit verstreut liegenden Häuser und Felder blciken zu können.<br /><br />
Gehzeit ca. 5 Std. Aufstieg 280 m, Abstieg 600 m

<br /><br />

<b style="color:#eb731a">Tag 10 - Ruhetag</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir verbringen den Tag geruhsam. Wandern durch den Ort und besichtigen die zwei alten Gompas und können die Layaps bei der täglichen Feldarbeit beobachten.<br /><br />

<b style="color:#eb731a">Tag 11 - Trekking nach Koina</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Schweren Herzens nehmen wir Abschied von dieser Oase des beschaulichen Lebens und wandern an einem Armeeposten vorbei abwärts bis zur Abzweigung, wo es nach Lunana geht, der höchsten bewohnten Region Bhutans. Durch den herrlichen Nationalpark geht es den Mo Chhu entlang in Richtung Koina, wo wir nach einem langen Tagesmarsch unser Camp errichten. (3.050 m)<br /><br />
Gehzeit ca. 7 - 8 Std. Abstieg 560 m
<br /><br />

<b style="color:#eb731a">Tag 12 - Trekking nach Gasa</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen erklimmen wir den letzten Bergrücken mit 3.900 m Bari La, bevor auf der anderen Seite vorbei an Wasserfällen und riesigen Bambusstauden der Blick frei wird auf den Ort Gasa mit seinem hoch aufragenden Dzong, vor der grandiosen Bergkulisse des Kang Bum (6.500 m). Gasa war bekannt für seine heißen Schwefelquellen, die aber in 2009 bei einem starken Erdrutsch zerstört wurden.<br /><br />
Gehzeit ca. 6 Std., Aufstieg 660 m, Abstieg 680 m
<br /><br />

<b style="color:#eb731a">Tag 13 - Gasa – Punakha</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der letzte Tag der Trekkingtour führt über den Ort Damji, vorbei an Terrassenfeldern und den ersten Bauernhäusern zur Straße, von wo uns das Fahrzeug nach Punakha bringt, wo wir wieder in einem bequemen Hotelbett schlafen können. Ein letzter Blick zurück zu den schneebedeckten Gipfeln, die einen bleibenden Eindruck hinterlassen haben und uns bewusst machen, warum die Berge für die Bhutanesen heilig sind.<br /><br />
Gehzeit ca. 3 - 4 Std.
