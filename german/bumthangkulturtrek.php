<b style="color:#eb731a">Tag 01 - Trekking nach Schonath</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Von Manchugang geht es bergauf zum Dorf Dhur, dem größten Dorf in Bumthang auf 2.850 m Höhe. Das Dorf hat ungefähr 75 Haushalte und die Bewohner sind überwiegend Bauern und Yakzüchter. Hier werden zwei unterschiedliche Dialekte gesprochen, Bumthang Kha und Brokke. Etwas unterhalb am Fluß kann man noch eine wasserbetriebene Mühle sehen, wo Getreide gemahlen wird. Das Camp liegt in Schonath auf 3.230 m Höhe. Hier hört man nachts des Öfteren den Ruf von Eulen.<br /><br />
Gehzeit ca. 4 – 5 Std.
<br /><br />

<b style="color:#eb731a">Tag 02 - Nächstes Camp Kitiphu</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Durch einen dichten Ur-Wald geht es am nächsten Morgen in ca. 2 Std. hinauf zum Drangela Pass (3600 m) und weiter am Bergrücken entlang auf 3.870 m, wo wir das Nachtlager errichten. Von hier aus hat man einen herrlichen Blick auf die schneebedeckten Gipfel in der Ferne und hinunter ins Tal. Wenn Sie noch etwas höher klettern auf 4000 m zum Gipfel Kitiphu, liegt das Gangkar Puensum Massiv direkt vor Ihnen.<br /><br />
Gehzeit ca. 5 – 6 Std.

<br /><br />

<b style="color:#eb731a">Tag 03 - Kitiphu – Jakar</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Vielleicht haben wir Glück und das Wetter beschert uns am Morgen einen unvergesslichen Sonnenaufgang. Anschließend geht es wieder abwärts zu den Klöstern Zhambala, Choedak und Tharpaling, wo uns die Mönche gerne willkommen heißen. Das Kloster Choedak beherbergt 100 Statuen von Chenrezig, was einzigartig ist in Bhutan. Am Nachmittag bringt uns ein gemächlicher Spaziergang entlang des Berghanges zurück nach Jakar, wo uns zum Abschluss noch ein herrlicher Blick auf den Dzong vergönnt ist.<br /><br />
Gehzeit ca. 4 Std.
