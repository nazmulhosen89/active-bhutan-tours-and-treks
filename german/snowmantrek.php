<div class="linkconttop">Snowman Trek [17 Nächte 18 Tage]</div>
<div style="float:left; padding:8px; height:auto; width:auto;">

Der Snowman Trek ist der schwierigste und längste Trek (18 Tage) und wird von vielen als schwierigster Trek der Welt angesehen, der nicht nur eine exzellente körperliche Verfassung sondern auch echte Abenteuerlust verlangt.<br />
Der Trek verläuft bis Laya wie der Laya Gasa Trek, danach wendet er sich nach Osten und überquert den Ganglakarchung Pass( 5.100m) in die Lunana Region, wo nur die Dörfer Thega und Chezo bewohnt sind. Es ist dies das entlegendste Gebiet Bhutans und mit 4.000m auch das am schwersten erreichbare. Die Einwohner sind Yakhirten, die für ihren schwierigen Charakter bekannt sind. Der Trek führt dann nach Süden und nach Überquerung des Rinchenzoe Passes(5.220m) gelangt man bei der Nikkarchho Brücke wieder auf eine feste Strasse.<br /><br />

