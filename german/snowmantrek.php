<b style="color:#eb731a">Tag 01 - Trekkingstart in Punakha</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Auf holpriger Straße geht es zum Ausgangspunkt der Trekkingtour nach Gesapang. Von dort sind es dann noch ungefähr 3 Stunden Aufstieg nach Gasa, einem kleinen Ort vor herrlicher Kulisse des Kang Bum. Falls noch Zeit bleibt besuchen wir noch den dortigen Dzong.
<br /><br />

<b style="color:#eb731a">Tag 02 - Überquerung des Bari La und weiter bis nach Chempsa	</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Heute beginnt der Aufstieg zum ersten Pass Bari La (3.710 m), höchster Punkt auf dem Weg nach Laya. Der Dschungel wird allmählich abgelöst von Pinienwäldern und kleine Bachläufe müssen überquert werden. Wir schlagen das Camp auf einer kleinen Lichtung (3.650 m) auf, die inmitten des Waldes liegt.<br /><br />
Gehzeit ca. 6 Std.

<br /><br />

<b style="color:#eb731a">Tag 03 - Heute erreichen wir Laya</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir steigen hinab bis zum Fluss Mo um einen Felsvorsprung zu umgehen. Am Fluß entlang geht es dann weiter unterhalb zerklüfteten Felsen auf einem herrlichen Weg mit vielen Vögeln und Schmetterlingen. Schließlich erreichen wir ein Militärcamp, wo unser Permit gecheckt wird. Ein schöner alter Chorten befindet sich am Eingang nach Laya, das auf 3.800 m liegt. Die Frauen tragen hier selbstgemachte spitze Bambushütchen und handgewebte Kleidung aus Yakhaar. Die Layaps sind emsig dabei die Ernte einzubringen und freuen sich über unseren Besuch.<br /><br />
Gehzeit ca. 7 Std.

<br /><br />

<b style="color:#eb731a">Tag 04 - Akklimatisationstag in Laya</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir wandern durch den Ort und können die Layaps bei der Feldarbeit beobachten. Außerdem besuchen wir die beiden alten Gompas.<br /><br />

<b style="color:#eb731a">Tag 05 - Aufbruch nach Lunana</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Ausgeruht verlassen wir Laya und wandern an einem Armeeposten vorbei abwärts bis zur Abzweigung des Lunana-Weges. Nach 40-minütigem steilem Aufstieg erreichen wir einen guten Aussichtspunkt mit Blick über das Mo Chhu- und Rhodo Chhu-Tal. Die Wälder entlang des Weges sind durch Brände stark geschädigt. Der beschwerliche Weg führt nun durch Koniferen- und Rhododendronwald im Tal des Rhodo Chhu aufwärts bis zur Baumgrenze. Vom oberen Teil eines großen Felsrutsches bietet sich ein fantastischer Blick auf ein breites Gletschertal, an dessen Ende ein Gletscher zu sehen ist, über dem sich der Tsenda Kang (7.100 m) erhebt. Nach Überquerung des Rhodo Chhu und ca. 8 Stunden Gehzeit kommen wir beim Lagerplatz Rhodophu (ca. 4.230 m) an. Wir haben das Reich des Schneeleoparden erreicht!<br /><br />
Gehzeit: 8h, Aufstieg: 1050m, Abstieg: 640m, Übernachtung im Zelt

<br /><br />

<b style="color:#eb731a">Tag 06 - Narethang</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Im Bereich des Himalaya-Hauptkammes wandern wir weiter Richtung Osten. Von unserem Lager geht es dem Fluss entlang und nach ca. 25 Minuten gelangen wir rechter Hand über einen Berghang zu einem großen offenen Tal. Hier steigen wir über Hochwiesen mit Geröll beständig aufwärts bis zur Passhöhe des Tsomo La (4.880 m), von der sich ein großartiger Blick auf den tibetischen Grenzbereich bietet; bei klarer Sicht können wir in einiger Entfernung auch die Chomolhari erkennen. Wir queren einige flache Hochtäler, unterbrochen nur von niederen Gratrücken und wandern zu unserem Lager Narethang (ca. 4.940 m), das vom Gangla Karchung (6.395 m) überragt wird. Wahrscheinlich steht uns eine kalte und windige Nacht bevor, wie auch in den nächsten Zeltlagern in etwa gleicher Höhe.<br /><br />

<b style="color:#eb731a">Geh Tag 07 - Tarina</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Schon am Vormittag werden wir auf dem Gangla Karchung La (ca. 5.220 m), dem höchsten Pass des nördlichen Tourenabschnittes, stehen. Wir benötigen ca. 3,5 Stunden, um einen Steinhaufen und die Gebetsfahnen auf der Passhöhe zu erreichen. Rundherum erheben sich hohe, eisgepanzerte Berge, die markantesten sind Kang Bum (6.526 m) im Westen sowie Tsenda Kang, Teri Gang (7.300 m) und Jejekangphu (7.100 m) in nördlicher Richtung. Der Abstieg - über 1.000 Höhenmeter - wird schwierig und zeitraubend.<br /><br />
Zunächst geht es über eine Moräne, die bei einer fast vertikalen Wand endet. Von hier bietet sich ein atemberaubender Blick auf den vom Teri Gang herunterkommenden Gletscher, dessen Abbruch oberhalb eines türkisblauen Gletschersees liegt. Die Wassermassen dieses Sees zerstörten bei einem Eissturz 1960 große Teile des Dzongs von Punakha und verwüsteten das Tal erheblich. Der weitere Abstieg über Moränenblockwerk enthält einige Steilstücke, die sehr vorsichtig passiert werden müssen.<br /><br />
Der untere Teil dieses Pfades führt durch Rhododendrongebüsch - bei Nässe oder Schnee ein mühsamer Weg! Im Tal angelangt wandern wir noch etwa 2 Stunden abwärts bis zum Tarina Camp.<br /><br />
Gehzeit: 7,5h, Aufstieg: 360m, Abstieg: 1400m, Übernachtung im Zelt<br /><br />
zeit: 7,5h, Aufstieg: 920m, Abstieg: 200m

<br /><br />

<b style="color:#eb731a">Tag 08 - Trekking zur Siedlung Woche</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir wachen auf in einem der schönsten Täler Bhutans umringt von schneebedeckten Bergen, Wälder und glasklare Flüsse. Wir folgen dem Fluss talwärts und machen an der Abzweigung nach Woche eine Mittagspause. Nach einem Anstieg von 300 m erreichen wir die kleine Ansiedlung, die nur aus 3 bis 4 Behausungen besteht. Da Ausländer nur sehr selten hierherkommen, werden wir natürlich herzlich begrüßt.<br /><br />
Gehzeit ca. 6 Std.
<br /><br />

<b style="color:#eb731a">Tag 09 - Überquerung des Keche La und weiter bis nach Lhedi</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Unser erstes Ziel ist der Keche La (4.580 m). Der ungefähr 3-stündige Weg dorthin führt uns erst durch Wald und dann über offenes Gelände mit herrlicher Aussicht auf Kangphu Gang und Jeje Kangphu (7.300m). Von dort geht es dann hinunter nach Thega, wo wir Mittagspause machen. Vor uns liegt ein riesiges bewaldetes Tal, das sich bis hin zu dem enormen "Tafelberg" Zongophu Gang (7.094 m) erstreckt. Wir folgen dem Flußbett des Po Chhu und erreichen nach ungefähr einer Stunde unser Camp in Lhedi, einer kleinen Ansiedlung, in der es auch eine Funkstation gibt.<br /><br />
Gehzeit ca. 7 Std.
<br /><br />

<b style="color:#eb731a">Tag 10 - Ins Herz der Lunana Region</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
Wir folgen wieder dem Fluss und kommen am Chorse Dzong vorbei, einem der ältesten Dzongs in Bhutan. Leute mit dicken Fellmützen begegnen uns und werfen uns neugierige Blicke zu. Von einer Anhöhe aus haben wir unseren ersten Blick hinunter auf Thanza, dem Hauptort der Region. Unser Camp befindet sich auf 4.000 m Höhe.<br /><br />
Gehzeit ca. 6 Std.
<br /><br />

<b style="color:#eb731a">Tag 11 - Ruhetag</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir legen einen Ruhetag ein und erkunden die Umgebung.
<br /><br />

<b style="color:#eb731a">Tag 12 - Trekking nach Tshorim</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Die heutige Etappe führt nach Tshorim (5.035 m), einem Camp an einem See gelegen, umgeben von schneebedeckten Eisriesen. Nachdem wir Thanza verlassen haben, können wir im Tal unten das Dorf Chozo mit seinem kleinen Dzong sehen und den Rauch, der von den kleinen Steinhäusern aufsteigt. Die Landschaft wird immer dramatischer und Wasserfälle donnern den Berg herunter. Übernachtung im Zelt<br /><br />
Gehzeit ca. 6 -7 Std.
<br /><br />

<b style="color:#eb731a">Tag 13 - Überquerung des Gohpu La</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Heute steht die Überschreitung des Gophu La (5.243 m) bevor, einem der wohl schönsten Passagen auf dem Trek. Wir starten früh da ein langer anstrengender Tag bevorsteht. Wenn der Pass erreicht ist, türmt sich am Horizont der höchste Berg Bhutan (Gankar Punsum 7.541 m) auf und man wird geblendet von der atemberaubenden Szenerie, die sich vor einem auftut. Nach einem langen ermüdenden Abstieg erreichen wir das Lager in Saram (4.830 m)<br /><br />
Gehzeit ca. 7 - 8 Std.
<br /><br />

<b style="color:#eb731a">Tag 14 - Trekking nach Minchugang</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir verlassen diesen herrlichen Flecken Erde und vorbei an Camps von Yakhirten trekken wir entlang des Tales, überqueren Flussläufe und haben immer wieder wunderbare Ausblicke auf die umliegenden Eisriesen. Das Lager errichten wir in Minchugang auf 4.210 m Höhe.<br /><br />
Gehzeit ca. 7 Std.
<br /><br />

<b style="color:#eb731a">Tag 15 - 3 Pässe liegen vor uns</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
Heute haben wir drei Pässe zu überqueren Phodrang La (4.652 m) Sake La (4.820 m) und am Schluß noch Uruthang La (4.780 m). Wir durchschreiten ein geologisch interessantes Gebiet mit roter Erde, die sich zu riesigen Dünen auftürmt. Das Camp in Uruthang (Warathang) liegt auf 4.455 m.<br /><br />
Gehzeit ca. 6 Std.
<br /><br />

<b style="color:#eb731a">Tag 16 - Dur Sachu</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Direkt oberhalb unseres Camps ist der Uli La (4.440 m), den wir zügig erreichen. Sie verlassen schön langsam die Hochgebirgsregionen, steigen ca. 1000 Höhenmeter ab und durchqueren dabei Wald und Dschungelgebiete von berauschender Schönheit. Am Schluß , überschreiten wir den Nephu La (.4495 m) bevor wir das Camp in Dur Sachu erreichen. Die Landschaft ist hier subtropisch und Vögel aller Art leben hier.<br /><br />
Gehzeit ca. 6 Std.

<br /><br />

<b style="color:#eb731a">Tag 17 - Badetag</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
Dur Sachu hat zwei "Gästehäuser" für Bhutanesen, die hierher kommen um die medizinische Wirkung des Wassers (heiße Schwefelquellen) zu genießen. Wir legen ebenfalls einen Badetag ein und erholen uns von den Strapazen der letzten Tage.<br /><br />

<b style="color:#eb731a">Tag 18 - Tsho Chenchen</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Heute steht wieder ein harter Trekkingtag bevor. Er beginnt mit einem langen Anstieg hinauf zum Djule La (4551 m) durch alpinen Wald und über den Gogden La (4.301 m). Auf der anderen Seite des Passes liegt vor Ihnen ein Tal und ein langer Marsch führt uns zu einem Camp von Yakhirten, wo wir in Tsho Chenchen (3.920 m) das Lager aufschlagen. <br /><br />
Gehzeit ca. 8 Std.
<br /><br />

<b style="color:#eb731a">Tag 19 - Trekking nach Gorsum</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der heutige Tag ist angenehm und der Trail führt meistens auf ebener Strecke durch alpinen Wald zum nächsten Nachtlager in Gorsum auf nur 3.100 m Höhe.<br /><br />
Gehzeit ca. 8 Std.
<br /><br />

<b style="color:#eb731a">Tag 20 - Dur</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Nach nur ca. 3 Std. Fußmarsch durch etwas schlammiges Gelände erreichen wir unseren Endpunkt der Trekkingtour in Dur, wo das Fahrzeug wartet. Von dort sind es nur ca.45 Min. Fahrzeit bis nach Jakar in Bumthang, wo wir in einem hübschen Gästehaus unsere Zimmer beziehen.<br /><br />



