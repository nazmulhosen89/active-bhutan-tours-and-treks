Sightseeing in Thimpu. Wir besuchen Memorial Chorten, eine riesige  Stupa, die in Gedenken an den Vater des heutigen Königs Jigme Singye Wangchuck,  erbaut wurde. In der Nationalbibliothek können wir alte Schriften bewundern. In  der Painting School können wir den Künstlern beim Herstellen der kostbaren  Thangkas beobachten. Besuch des Handwerkszentrums, wo Sie die Möglichkeit  haben, typisch bhutanesische Textilien oder Kunsthandwerk zu erstehen.  Nachmittags Fahrt zum Simtokha Dzong, den ältesten Dzong Bhutans, der 1629 von  Shabdrung Ngawang Namgyal erbaut wurde. Weiterfahrt zu Pangri Zampa Monestery,  das sich nahe Dechencholing Palace befindet.<br>
