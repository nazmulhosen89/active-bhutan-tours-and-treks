Sightseeing in Paro. Wir besuchen die Ruinen des Drugyal Dzong, die sich  in 16 km Entfernung von Paro befinden. Von dieser Festung aus haben die  Bhutanesen mehrere Invasionen aus Tibet erfolgreich verhindert. Daher stammt  auch der Name &quot;victorious Bhutanese&quot;. Von der Festung aus haben wir  einen herrlichen Blick auf Mount Chomolhari, &quot;Mountain of Goddess&quot;,  der 7.329 Meter hoch ist! Auf der Rückweg besuchen wir ein typisches  Bauernhaus. Nachmittags besuchen wir das National Museum und den Paro Rinpung  Dzong, der wie alle Dzongs den Sitz der Verwaltung und ein Kloster beherbergt.  Abends fahren wir weiter nach Thimpu.<br><br>
