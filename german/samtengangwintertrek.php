<b style="color:#eb731a">Tag 01 - Punakha Dzong - Limukha</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen besuchen Sie erst noch den Dzong in Punakha, der auf einer Landzunge liegt, die von zwei Flüssen umspült wird. Der Dzong gehört mit zu den beeindruckendsten Bauwerken Bhutans und seine Gebetshalle ist ein Meisterwerk an einheimischer Handwerkskunst.
Kurz hinter dem Dzong, vorbei am Krematorium des Tales geht es zuerst über die längste Hängebrücke Bhutans zu einem kleinen Dorf und von dort steigt der Weg an durch Terrassenfelder und Pinienwald hinauf zu einem kleinen Pass in 2.200 m Höhe mit einem alten Chorten und einigen Gebetsfahnen. Wo sich der Wald lichtet ergeben sich immer wieder schöne Ausblicke auf das Punakha Tal, den Dzong und evtl. sogar auf die schneebedeckten Gipfel der hohen Berge im Norden. Vom Pass aus ist bereits das Dorf Limukha zu sehen, wo Sie in der Nähe das erste Camp aufschlagen. In früheren Zeiten soll hier ein großer See gewesen sein und auch ein König soll hier gelebt haben. Jetzt ist das Dorf umgeben von Reisfeldern und teilweise undurchdringlichem Bergwald.<br /><br />
Gehzeit ca. 4 Std. (9 km)
<br /><br />

<b style="color:#eb731a">Tag 02 Auf zum heiligen See</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der zweite Tag der Trekkingtour führt durch Waldgebiete mit Eichen und Rhododendren und vielen Vögeln, vorbei an vereinzelten Gehöften in ca. 2 Std. zu einem weiteren kleinen Pass (Dochen La 2.645 m). Von dort aus hat man eine herrliche Sicht auf den Bergrücken mit dem Dorf Samtengang, wo das zweite Camp liegt. Der Weg führt vorbei an dem Kloster Dargyel Gompa, wo der bekannte Heilige Drukpa Kinley für einige Zeit gelehrt hat.<br /><br />
Im kleinen Dorf Chungsakha genießen Sie das mitgebrachte Mittagessen unter einem großen alten Baum, bevor es noch einmal ca. 2 Std. steil bergauf geht zum herrlich gelegenen Camp in Samtengang (2.050 m hoch)<br /><br />
Gehzeit ca. 6 - 7 Std. (14 km)

<br /><br />

<b style="color:#eb731a">Tag 03 - Umgebung Samtengang</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am dritten Tag erkunden Sie die historisch sehr interessante Umgebung um die sich zahlreiche Geschichten ranken. Als es noch keine Straße gab, verbrachten die Reisenden hier ihre erste Nacht auf dem Weg von Wangdue Phodrang in Richtung Trongsa. In der Nähe des Camps liegt ein kleiner See (Lutshokha) zu dem es ebenfalls eine Legende gibt. Am Morgen Spaziergang hinauf zum kleinen Dorf Samtengang mit seinem schönen Dorfplatz und einem großen traditionellen Bauernhaus, das Sie wenn möglich besichtigen.<br /><br />
Von dort es führt eine schöne Wanderung zum großen Kloster Sagang, in dem bisher nur sehr wenig Touristen waren. Besuch des Klosters und Rückweg auf dem Bergrücken zum Camp. Auf dem Weg liegen immer wieder schöne Bauernhäuser, terrassenförmig angelegte Felder und eine kleine Schule.
<br /><br />

<b style="color:#eb731a">Tag 04 - Samtengang – Tika Zam</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen starten Sie noch zu einem schönen Spaziergang von ca. 1 Std. hinunter ins Tal nach Tika Zam, wo das Fahrzeug an der Hauptstraße wartet. Von hier aus können Sie weiter nach Gangte/Trongsa oder zurück nach Wangdue Phodrang fahren.<br /><br />



