<div class="linkconttop">Trekking-Informationen für Bhutan</div>
<div style="float:left; padding:8px; height:auto; width:auto;">
Trekking ist die beste Möglichkeit, die Seele Bhutans zu erleben. Es  ermöglicht einen Einblick in die ländliche Kultur des Landes, durch den Kontakt  mit Bewohnern in entlegenen Dörfern, einsamen Camps von Yakhirten und weltentrückten  kleinen Klöstern. <br><br>
  Die beste Zeit für eine Trekkingtour in Bhutan ist entweder von Mitte März  bis Anfang Mai, wenn die Rhododendren blühen, oder im Herbst von Mitte Sept.  bis Ende Okt., wo Enzian und Edelweiß die Hänge überziehen. Aber auch in den  Monaten Juli/August lohnt sich eine Tour, da dann auf den Hängen unzählige  Blumen in allen Farben sprießen, vom blauen Mohn bis hin zum Berglotus.  Zwischendurch muß man im Sommer mit Regenschauern rechnen, aber meist erst in  den Abendstunden. <br><br>
  Im Herbst sind die Nächte bereits kühler (von +5 Grad bis teilweise - 5 Grad  in sehr hohen Lagen), während am Tag immer noch angenehme Temperaturen um die  18 Grad herrschen, die das Trekken sehr angenehm gestalten. <br><br>
  Im Mai/Juni sind die Nächte schon etwas wärmer, dafür ist der Himmel nicht so  klar und die Berggipfel sind oftmals in Wolken gehüllt. <br><br>
  Alle Trekkingtouren werden mit Zelten, einer Campingausrüstung und der  notwendigen Begleitmannschaft, die aus dem Guide, dem Koch und je nach  Teilnehmerzahl aus mehreren Helfern und Pferdeführern besteht, durchgeführt.  Das Gepäck und die Ausrüstung werden auf Packpferde - in höheren Regionen  manchmal auf Yaks - verladen, die unter Umständen mehrmals gewechselt werden  müssen, da jeder Distrikt über seine &quot;eigenen Transporteure&quot; verfügt  und hier bestimmte Regeln einzuhalten sind.<br><br>
  
