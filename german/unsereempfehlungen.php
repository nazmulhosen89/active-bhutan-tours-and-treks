 <b style="color:#eb731a">Besichtigungsreise</b> <br />
Koffer oder große Reisetasche, max. 20kg, da das Gewicht bei Druk Air genau kontrolliert wird.<br /><br />

 <b style="color:#eb731a">Trekken</b> <br />
Eine große weiche Reisetasche mit mehreren Griffen ist besser als ein großer Rucksack. Die Reisetasche wird beim Trek in einem großen Sack verstaut und auf den Pferden transportiert. Sie finden ihre Tasche am Ende jedes Tages in ihrem Zelt im Camp vor. Nehmen Sie Plastiksäcke mit, in denen Sie Ihre Toiletteartikel und Wäsche separat verstauen können, damit sie diese im dunklen Zelt leicht finden. Für den Trek ist ein kleinerer Tagesrucksack ausreichend.<br /><br />

