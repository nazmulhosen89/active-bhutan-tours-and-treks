Viele Besucher kommen nach Bhutan, um die religiösen Feste mitzuerleben. Die Tsechus, die jährlich in den Dzongs oder in der Nähe von Heiligtümern stattfinden, bieten ein faszinierendes Schauspiel, das gewöhnlich mehrere Tage dauert. Höhepunkt ist meist die Enthüllung eines Thangkas, eines riesigen farbenprächtigen Rollbildes, auf dem Guru Rinpoche und seine acht Verkörperungen zu sehen sind. Zu den bekanntesten Tsechus zählen jene in Thimpu, Paro und Bumthang. Aber auch in kleineren Städten oder Dörfern finden solche Feste zu Ehren Guru Rinpoches statt. <br /><br />
Active Bhutan Tours hat sich auf das Besuchen von Festivals in entlegenen kleinen Dörfern wie Gom Kora, Chorten Kora oder Punakha Sedra spezialisiert. Hier finden sich nur wenige Touristen ein und die Maskentänze und religiösen Gesänge können in ihrer ganzen Ursprünglichkeit miterlebt werden. Eine gute Gelegenheit, die Lebensweise der Bhutanesen näher kennenzulernen.<br /><br />
