<b id="6" style="color:#eb731a">Hochzeit</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
     <img src="../images/hochzeit.png" style="margin-left:8px; float:right; border:#ed95b9 solid 1px; padding:2px;" />   

Traditionelle bhutanische Hochzeiten finden in einem Dzong oder Tempel statt. Lamas vollführen zahlreiche Rituale bis das Paar formell verheiratet wird. Nach der Verteilung von "Doma" (Betelnüsse), werden das Brautpaar und deren Gäste mit Erfrischungen und herrlichem Essen verwöhnt und mit traditionellen Gesängen und Tänzen unterhalten. Die Hochzeitszeremonie endet mit dem traditionellen Schlusstanz "Tashi Laybey". Eine Hochzeit in Bhutan ist ein großes Fest!
