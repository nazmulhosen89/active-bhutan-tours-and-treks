<b style="color:#eb731a">Tag 01 - Trekking nach Sharna</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Kurze Fahrt zur Ruine des Drukgyel-Dzong, wo die Trekkingtour beginnt. Das Gepäck wird wasserdicht verpackt und auf Packpferde verladen. Leichte Wanderung im lieblichen Hochtal des Paro Chu (Chu=Fluss). Auf beiden Seiten des Flusses verläuft der Weg durch Terrassenfelder und Wiesen, vorbei an kleinen Ansiedlungen und einem Armeeposten der unser Permit checkt. Am frühen Nachmittag erreichen wir unser erstes Camp in Sharna. (2.870 m).<br /><br />
Gehzeit ca. 5 Std; Aufstieg 515 m, Abstieg 220 m.
<br /><br />

<b style="color:#eb731a">Tag 02 – Thangthangkha</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Früh brechen wir zu unserer ersten längeren Wanderung auf. Durch einen Wald von Kiefern, Ahorn, Eichen und Rhododendren geht es flussaufwärts durch den Jigme Dorji National Park vorbei an Chorten und über kleine Brücken. Ca. 2 Std. sind es bis zum Steinhaus Shing Karapa (3.110 m) auf einer Lichtung. Bald danach erreichen wir die Abzweigung zum Tremo La, dem alten Handelsweg zwischen Tibet und Bhutan. Heute können wir den ersten Blick auf Jhomolhari (7314 m) werfen, die Herrin der Götterberge und heiligsten Berg des Landes. Die schneebedeckte Kuppel taucht plötzlich vor uns auf, wie aus dem Nichts. Unser Lager errichten wir in Thangthangkha auf 3.630 m.<br /><br />
Gehzeit ca. 7 Std. , Aufstieg 900 m, Abstieg 150 m.

<br /><br />

<b style="color:#eb731a">Tag 03 - Jhomolhari Basecamp</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Vorbei an den ersten Yakherden, die friedlich auf den Wiesen am Fluß grasen, Manimauern und vereinzelten kleinen Bauernhäusern geht es zum Fuße des Jhomolhari-Massivs, wo wir vor der Ruine eines verlassenen Forts das Lager aufschlagen. Yakzucht gehört zur Lebensgrundlage der hier ansässigen Bewohner. Aus Yakmilch wird außerdem Käse und Butter hergestellt. Steinerne Chorten umgeben das Lager und ein kleiner Bach durchquert den Lagerplatz in Jangothang auf 4.040 m.<br /><br />
Gehzeit ca. 5 – 6 Std; Aufstieg 550 m, Abstieg 100 m.
<br /><br />

<b style="color:#eb731a">Tag 04 - Am Fuße der Göttin Jhomo</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen können wir den Sonnenaufgang direkt von unserem Zelt aus genießen. Entspannungstag im Lager mit der Möglichkeit zu Ausflügen zu einem hochgelegenen See bzw. zum Aufstieg einer der vorgelagerten Gipfel mit Blick auf Jhomolhari (7314m) und Jichu Drake (6989m)<br /><br />

<b style="color:#eb731a"> Tag 05 - Wir überqueren den Bonte La</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Wir verlassen das Tal und gelangen zu den Tsho Phu Lakes, in denen sich bei klarem Wetter der Gipfel des Jichu Drake im Wasser spiegelt. Hier haben meistens Yakhirten ihre Lager aufgeschlagen die Butter und Käse aus Yakmilch herstellen. Ein anstrengender Aufstieg zum Bonte La (4.890) liegt vor uns, aber es lohnt sich, da sich oben eine bizzare Berglandschaft ausbreitet. Anschließend folgt ein langer Abstieg ins gegenüberliegende Tal. Im Herbst sind die Hänge übersät mit zahlreichen verschiedenen Blumen, wie z.B. dem Edelweiß und dem blauen Enzian. Das Camp errichten wir im Soi Yaksey Valley, einem wunderschönen Tal mit Wasserfällen und mächtigen Felsen, umgeben von schneebedeckten Berggipfeln. Lediglich ein paar verstreute Bauernhäuser durchziehen das ursprünglich anmutende Gebirgstal. Unsere Ankunft bleibt in der Regel nicht unbemerkt und bald kommen die ersten neugierigen Besucher in unser Camp.<br /><br />
Gehzeit 7 - 8 Std.

<br /><br />

<b style="color:#eb731a">Tag 06 - Tangbue</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Durch einen Wald von Birken und Eichen steigt der Weg an zum Tagulun La (4.540 m) wo wir einen herrlichen Blick auf das hinter sich liegende Tal haben. Bei klarem Wetter können wir sogar Kanchenjunga und Makalu sehen. Das Camp liegt in Tangbue (4.120 m ) einer Hochebene, auf der überwiegend Yaks und Pferde weiden.<br /><br />
Gehzeit 5 – 6 Std.

<br /><br />

<b style="color:#eb731a">Tag 07 - Zurück nach Paro</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Nachdem wir nach einem letzten kurzen Anstieg den Thombu La (4270 m) erreicht haben, geht es auf einem malerischen Höhenweg hinunter nach Sharna und von Dort auf gleicher Strecke wieder zurück zum Drukgyel Dzong. <br /><br />
Gehzeit ca. 7 Std.
