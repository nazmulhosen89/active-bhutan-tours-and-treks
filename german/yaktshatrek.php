<div class="linkconttop">Yaktsha Trek [6 Nächte 7 Tage]</div>
<div style="float:left; padding:8px; height:auto; width:auto;">

Dieser sensationelle 7 Tagestrek führt in das Land der Yakhirten im Norden Bhutans. Am Fuße des Jumolhari ( 7.314m) haben sie einen einzigartigen Blick auf Berge und die besondere Pflanzenwelt  Bhutans. Sie werden viele Yakherden mit ihren Hirten begegnen, aber wenige Dörfer durchqueren. Die höchste Stelle des Treks liegt auf 4.700m. Der Yaksha Trek ist vielleicht der schönste Trek in Bhutan und kann von September bis November und im April und Mai gemacht werden.<br /><br />
