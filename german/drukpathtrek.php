<div class="linkconttop">Druk Path Trek [4 Nächte 5 Tage]</div>
<div style="float:left; padding:8px; height:auto; width:auto;">

Dieser kurze Trek führ von Paro nach Thimpu/oder umgekehrt. Er führt über die Bergkette, die beide Täler trennen. Entlang des Weges finden sich wenige Häuser, auf 4.000m sind herrliche Seen mit großem Fischbestand und im Mai sind die Rhododendren in voller Blüte zu erleben. Bei klarer Sicht ist der hohe Himalaya entlang des ganzen Treks zu sehen. Dieser Trek kann zu jeder Jahreszeit gemacht werden, die beste Zeit ist von Mitte September bis Ende Mai.<br />
