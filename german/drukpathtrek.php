<b style="color:#eb731a">Tag 01 Über den ersten Pass</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der erste Trekkingtag ist etwas anstrengend, da es hinauf geht auf 4.000 m Höhe.<br /><br />
Der Trek beginnt mit einer kurzen Fahrt hinauf zum Radio Tower über Thimphu, wo Sie unzählige bunt flatternde Gebetsfahnen empfangen. Nach ca. 1 Std. erreichen Sie das kleine Kloster Chhokhortse (3.010 m) in das sich oftmals Mönche zum Retreat zurückziehen. Von dort aus geht es dann erst mal gemächlich einen kleinen Pfad entlang, bevor der Weg wieder steiler ansteigt. Schließlich erreichen Sie eine kleine Lichtung, wo ein zweiter Weg von Thimphu heraufführt, den meistens die Pferde mit dem Gepäck benutzen.<br /> <br />
Der Weg wird nun ziemlich steil und man merkt, dass man sich bereits auf 3.400 m Höhe bewegt. Glücklicherweise taucht nun ein großer Chorten auf, wo eine kleine Rast eingelegt wird. Nicht allzu weit ist nun bereits der Kloster-Komplex von Phajoding (3.690 m) zu sehen, wo Sie das mitgebrachte Mittagessen geniessen. Diverse Gebäude sind auf dem Berghang verstreut und ein kurzer Besuch im alten Hauptkloster lohnt sich. Hinter dem Gebäude geht es nochmal ca. 1 ½ Std. steil bergauf zum Passhöhe (4.120 m), wo eine alte Stupa steht und kleine Gebetsfähnchen wehen. Ab jetzt ist der schwierige Teil geschafft aber bis zum Campground ist es noch eine Weile zu gehen. Auf einer Hochebene geht es nun in mehreren Auf und Abs u.a. über den 4.200 m hohen Labanah Pass in ca. 2 Std. bis zu Ihrem ersten Camp, das zwischen den Bergen eingebettet liegt.<br /><br />
Gehzeit ca. 7 Std.
<br /><br />

<b style="color:#eb731a">Tag 02 Auf zum heiligen See</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Am Morgen haben Sie erst wieder einen kurzen Anstieg zu einem kleinen Pass vor sich, von wo aus es hinunter geht zum ersten kleinen See (Simkotra Tsho 4.090 m), wo Ihnen evtl. die ersten Yaks begegnen. Von dort aus geht es weiter auf der Hochebene zwischen unzähligen Rhododendronbüschen zum nächsten kleinen Bergsee (Jane Tsho 3.956 m). Von hier aus wäre ein Abstieg ins Tal von Tshaluna möglich, aber Sie bleiben auf der Hochfläche, überqueren nochmal zwei kleine Pässe, bis dann endlich der Jimilang Tsho (3.880 m) vor Ihnen auftaucht, wo Sie Ihr zweites Camp aufschlagen.<br /> <br />
Gehzeit ca. 5 – 6 Std.

<br /><br />

<b style="color:#eb731a">Tag 03 Trek zum Jele Dzong</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der heutige Weg führt überwiegend am Bergkamm entlang mit herrlichen Ausblicken auf die Umgebung bis hin zu Jhomolhari (7314 m) und hinunter ins Parotal. Camps von Yakhirten liegen auf Ihrem Weg und wenn Sie möchten können Sie gerne einen Blick in die einfache Behausung werfen. Schließlich taucht der kleine Jele Dzong in der Ferne auf, wo sich etwas unterhalb ein schöner Campingplatz befindet. <br /><br />
Gehzeit ca. 7 – 8 Std.
<br /><br />

<b style="color:#eb731a">Tag 04 Hinunter nach Paro</b> 
  <div style="mso-element: para-border-div; border-left: medium none; border-right: medium none; border-top: medium none; border-bottom: 2.25pt double #fdf5a3; padding-bottom: 4.0pt; margin-bottom:6px; margin-top:4px;"></div>
   
Der letzte Trekking-Tag ist nur ein kurzer Spaziergang von ca. 3 Std. bergab nach Paro.
Am Morgen sollten Sie erst noch den kleinen Jele Dzong besuchen, in dessen schönem Gebetsraum u.a. eine riesige Statue von Buddha zu sehen ist. Der Weg führt dann erst ca. 1 Std. durch herrlichen und ursprünglichen Hochwald, bevor sich der Wald lichtet und sich eine lange Mani Mauer entlang zieht. Vereinzelt tauchen nun trad. Bauernhäuser auf, die von Apel-Plantagen umgeben sind. Die Gegend nennt sich Damchena und liegt auf 2.900 m Höhe. Auf einer sandigen Fahrstraße geht es dann weiter hinunter und schöne Blicke auf das Parotal und die umliegenden Berge tun sich auf. <br /><br />
Schließlich ist der Endpunkt des Treks, das Nationalmuseum in Paro, erreicht, wo Sie sich von Ihrer Trekking-Mannschaft verabschieden müssen.
